class Gallery extends Component {
  state = {
    imageDetails: imagesList[0],
  }

  updateThumbnail = id => {
    const filterDetails = imagesList.filter(eachItem => eachItem.id === id)

    this.setState({
      imageDetails: filterDetails[0],
    })
  }

  render() {
    const {imageDetails} = this.state
    const {imageUrl, imageAltText} = imageDetails
    return (
      <div className="app-container">
        <div className="gallery-container">
          <img className="image" src={imageUrl} alt={imageAltText} />
          <h1 className="main-heading">Nature Photography</h1>
          <p className="note">Nature Photography By Rahul</p>
          <ul className="thumbnailItem-container">
            {imagesList.map(eachItem => (
              <ThumbnailItem
                thumbnailDetails={eachItem}
                updateThumbnail={this.updateThumbnail}
                isActive={imageDetails.id === eachItem.id}
                key={eachItem.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
